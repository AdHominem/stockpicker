function BalanceSheet() {

    function analyze() {
        // Find last LT debt increase year
        let lastLTDebtIncreaseReport;
        for (let i = 0; i < balanceSheet.annualReports.length; i++) {
            if (balanceSheet.annualReports[i].longTermDebt > balanceSheet.annualReports[i + 1]) {
                lastLTDebtIncreaseReport = balanceSheet.annualReports[i]
            }
        }

        // If there was none, that means LT debt never increased!
        balanceSheet.ltDebtIncreasing = false;

        console.log(lastLTDebtIncreaseReport);
    }

    analyze();

    return (
        <div className="App">
            <h2>Balance Sheet</h2>
            <h3>Long Term Debt</h3>
            {balanceSheet.ltDebtIncreasing ? <div>LT debt has been increasing.</div>
                : <div>LT debt is not increasing.</div>}
            <table>
                <tbody>
                <tr>
                    <th>Date</th>
                    <th>LT Debt</th>
                </tr>
                {balanceSheet.quarterlyReports.slice(0,4).map(entry =>
                    <tr key={entry.fiscalDateEnding}>
                        <td>{entry.fiscalDateEnding.slice(0, 7)}</td>
                        <td>{formatNumber(entry.longTermDebt)}</td>
                    </tr>
                )}
                </tbody>
            </table>

            <h3>Share Buyback</h3>
            {balanceSheet.annualReports[0].reportedCurrency === "CNY" ? <p>This company only issues ADRs. Shares outstanding should be
                taken with a grain of salt as ADRs and share numbers can be confused.</p> : <div/>}
            <table>
                <tbody>
                <tr>
                    <th>Date</th>
                    <th>Shares Outstanding</th>
                </tr>
                {balanceSheet.quarterlyReports.slice(0,4).map(entry =>
                    <tr key={entry.fiscalDateEnding}>
                        <td>{entry.fiscalDateEnding.slice(0, 7)}</td>
                        <td>{formatNumber(entry.commonStockSharesOutstanding)}</td>
                    </tr>
                )}
                </tbody>
            </table>

            <h3>Debt-to-Equity Ratio</h3>
            {balanceSheet.quarterlyReports[0].totalLiabilities / balanceSheet.quarterlyReports[0].totalShareholderEquity}
            <h3>Cash per Share</h3>
            {balanceSheet.quarterlyReports[0].cashAndCashEquivalentsAtCarryingValue / balanceSheet.quarterlyReports[0].commonStockSharesOutstanding}
        </div>
    )
}

