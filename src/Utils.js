export const createAnnualReports = (earnings, income, balance, cashflow) => {

    let result = {};

    console.log(earnings.annualReports);
    console.log(income.annualEarnings);
    console.log(balance.annualReports);
    console.log(cashflow.annualReports);

    income.annualReports.forEach(incomeEntry =>
        cashflow.annualReports.forEach(cashflowEntry =>
            balance.annualReports.forEach(balanceEntry => {
                earnings.annualEarnings.forEach(earningsEntry => {
                    if (incomeEntry.fiscalDateEnding === cashflowEntry.fiscalDateEnding) {
                        if (incomeEntry.fiscalDateEnding === balanceEntry.fiscalDateEnding) {
                            if (incomeEntry.fiscalDateEnding === earningsEntry.fiscalDateEnding) {
                                result[incomeEntry.fiscalDateEnding] = {};
                                result[incomeEntry.fiscalDateEnding].income = {...incomeEntry};
                                result[incomeEntry.fiscalDateEnding].cashflow = {...cashflowEntry};
                                result[incomeEntry.fiscalDateEnding].balance = {...balanceEntry};
                                result[incomeEntry.fiscalDateEnding].earnings = {...earningsEntry};
                            }
                        }
                    }
                })
            })
        ))

    // Normalize Prices
    // Prices can be wrong since they dont refer to fiscal dates but trading dates! So a fiscal date can be 03-31 while
    // the trading date is 03-29. Thus, the day has to be omitted
    // Also, only the closing price is relevant
    const pricesNormalized = {};

    for (const [key, value] of Object.entries(prices)) {
        pricesNormalized[key.slice(0, 7)] = value["4. close"];
    }

    // Add prices
    for (const [key, value] of Object.entries(result)) {
        value.price = pricesNormalized[value.income.fiscalDateEnding.slice(0, 7)]
    }


    console.log(result);

    return result;
}

