function getGrowth(thisPeriodEPS, lastPeriodEPS) {
    return Math.trunc(-100 * (1 - thisPeriodEPS / lastPeriodEPS));
}

/**
 * Takes a growth rate in raw form (1.43, 0.83) and translates it to readable form (43%, -17%)
 * @param growthRate
 */
function formatGrowthRate(growthRate) {
    return Math.trunc((growthRate - 1) * 100);
}

function calculateGrowth(reports) {
    const earnings = Object.values(reports.annually).map(report => report.earnings);
    for (let i = 0; i < earnings.length; i++) {
        const thisYear = earnings[i];
        const lastYear = earnings[i + 1];
        if (lastYear && thisYear.reportedEPS > 0 && lastYear.reportedEPS > 0) {
            earnings[i].growth = getGrowth(thisYear.reportedEPS, lastYear.reportedEPS);
        }
    }

    // Quarterly growth
    const quarterlyEarnings = Object.values(reports.quarterly).map(report => report.earnings);
    for (let i = 0; i < quarterlyEarnings.length - 4; i++) {
        quarterlyEarnings[i].growthYoY = formatGrowthRate(quarterlyEarnings[i].reportedEPS / quarterlyEarnings[i + 4].reportedEPS)
    }
}

const calculatePE = (reports) => {
    const annualReports = Object.values(reports.annually);
    annualReports.forEach(report => report.pe = report.price / report.earnings.reportedEPS);

    const quarterlyReports = Object.values(reports.quarterly);
    quarterlyReports.forEach(report => report.pe = report.price / 4 / report.earnings.reportedEPS);
}

const normalizeNumbers = (report) => {


    for (const [key, value] of Object.entries(report)) {
        // Replace "none" fields with 0
        if (value === "None") {
            report[key] = 0;
        }
        // Truncate all dates to omit days as they are not important
        if (key === "fiscalDateEnding") {
            report[key] = value.slice(0, 7);
        }

    }
    return report;
}

