const ONE_THOUSAND = 1000;
const ONE_MILLION = ONE_THOUSAND * ONE_THOUSAND;
const ONE_BILLION = ONE_THOUSAND * ONE_MILLION;

export const formatNumber = (number) => {

    let result = 0;
    if (number < ONE_THOUSAND) {
        result = number.toFixed(2);
    } else if (number < ONE_MILLION) {
        result = (number / ONE_THOUSAND).toFixed(2) + "K"
    } else if (number < ONE_BILLION) {
        result = (number / ONE_MILLION).toFixed(2) + "M"
    } else {
        result = (number / ONE_BILLION).toFixed(2) + "B";
    }
    return result;
}

